#include "3-calc.h"

/**
 * op_add - add fun
 *
 * @a: int
 * @b: int
 * return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub fun
 *
 * @a: int
 * @b: int
 * return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul -mul fun
 *
 * @a: int
 * @b: int
 * return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div fun
 *
 * @a: int
 * @b: int
 * return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod fun
 *
 * @a: int
 * @b: int
 * return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
